package com.example.muscleup.model.data;

public class UserProfile {
    private String name;
    private int age;
    private String sex;
    private int height;
    private int weight;
    private byte[] image;

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }


    public String getSex() {
        return sex;
    }


    public int getHeight() {
        return height;
    }


    public int getWeight() {
        return weight;
    }


    public byte[] getImage() {
        return image;
    }
}
